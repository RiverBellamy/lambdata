'''various helper functions for data science tasks'''
import sklearn.model_selection


def null_count(df):
    '''returns the total number of null values in a dataframe'''
    return df.isnull().sum().sum()


def train_test_split(df, frac=.8):
    '''returns training and test sets, with frac being the fraction of the
    data to set aside for training'''
    if frac < 0 or frac > 1:
        print(f'{frac} is not a valid fractions, fractions must be between 0 and 1')
        return None
    return sklearn.model_selection.train_test_split(df, train_size=frac)


